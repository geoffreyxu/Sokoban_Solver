# next_states returns the list of successor states of a state s. It does this by repeatedly calling try_move for
# all four directions that the player can move in.
def next_states(s):
    row, col = getKeeperPosition(s)
    s_list = []
    dirs = ["N", "E", "S", "W"]
    for dir in dirs:
        s1 = np.copy(s)
        s_list.append(try_move(s1, dir, row, col))


    return cleanUpList(s_list)

