# try_move takes a state, row and column indices, and a direction. It then attempts a move in that direction
def try_move(S, D, r, c):
    dimRow = S.shape[0]
    dimCol = S.shape[1]
    spacePush = True
    moveLegal = False

    if D == "N":
        dir = r - 1
        nSquare = [dir, c]
        nnSquare = [r - 2, c]
    elif D == "E":
        dir = c + 1
        nSquare = [r, dir]
        nnSquare = [r, c + 2]
    elif D == "S":
        dir = r + 1
        nSquare = [dir, c]
        nnSquare = [r + 2, c]
    elif D == "W":
        dir = c - 1
        nSquare = [r, dir]
        nnSquare = [r, c - 2]

    if nnSquare[0] >= dimRow or nnSquare[0] < 0 or nnSquare[1] >= dimCol or nnSquare[1] < 0:
        spacePush = False
        nextnextSquare = -1
    else:
        nextnextSquare = S[nnSquare[0], nnSquare[1]]
    if nSquare[0] >= dimRow or nSquare[0] < 0 or nSquare[1] >= dimCol or nSquare[1] < 0:
        return S
    nextSquare = S[nSquare[0], nSquare[1]]

    # If the square that the player is attempting to move onto is blank or is a goal, then move the player onto it.
    # If the square that the player is attempting to move onto is a wall, return without moving
    if isBlank(nextSquare):
        set_square(S, nSquare[0], nSquare[1], 3)
        moveLegal = True
    elif isStar(nextSquare):
        set_square(S, nSquare[0], nSquare[1], 6)
        moveLegal = True
    elif isWall(nextSquare):
        return S

    # If the square that the player is attempting to move onto is a box, then push the box if possible
    elif isBox(nextSquare):
        if isBlank(nextnextSquare):
            set_square(S, nnSquare[0], nnSquare[1], 2)
        elif isStar(nextnextSquare):
            set_square(S, nnSquare[0], nnSquare[1], 5)
        elif isWall or not spacePush:
            return S
        set_square(S, nSquare[0], nSquare[1], 3)
        moveLegal = True
    
    # If the square that the player is attempting to move onto is a goal with a box in it, then push the
    # box if possible.
    elif isBoxstar(nextSquare):
        if isBlank(nextnextSquare):
            set_square(S, nnSquare[0], nnSquare[1], 2)
        elif isStar(nextnextSquare):
            set_square(S, nnSquare[0], nnSquare[1], 5)
        elif isWall or not spacePush:
            return S
        set_square(S, nSquare[0], nSquare[1], 6)
        moveLegal = True

    # If the player made a move, update the previous position so that the player no longer occupies it.
    if moveLegal:
        if isKeeper(S[r, c]):
            S[r,c] = 0
        elif isKeeperstar(S[r,c]):
            S[r,c] = 4    
    return S
    
    


