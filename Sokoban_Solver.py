# find_dist_to_goal takes a state and row and column indices, and finds the distance to the nearest goal.
# It iterates through every index in the state and for each goal, calculates the Manhattan distance to the
# goal from the given coordinates
def find_dist_to_goal(S, r, c):
    minDist = 1000
    dimRow = S.shape[0]
    dimCol = S.shape[1]
    for i in range(dimRow):
        for j in range(dimCol):
            if isBoxstar(S[i, j]) and i == r and j == c:
                return 0
            elif isStar(S[i, j]): 
                minDist = min(minDist, abs(i - r) + abs(j - c))
    return minDist

# isDeadlocked takes a state and row and column indices, and returns whether the object at those indices
# is able to be moved. If not, it returns false. It does this by seeing if at least 2 sides of a box are 
# against a wall in the current state. This means it cannot be moved.
def isDeadlocked(S, r, c):
    if isBoxstar(S[r, c]):
        return False
    dimRow = S.shape[0]
    dimCol = S.shape[1]
    wall_north = 0
    wall_south = 0
    wall_east = 0
    wall_west = 0
    if r == 0:
        wall_north = 1        
    if r == dimRow - 1:
        wall_south = 1
    if c == dimCol - 1:
        wall_east = 1
    if c == 0:
        wall_west = 1
    if (wall_north or wall_south) and (wall_east or wall_west):
        return True
    if not wall_north:
        wall_north = S[r - 1, c]
    if not wall_south:
        wall_south = S[r + 1, c]
    if not wall_east:
        wall_east = S[r, c + 1]
    if not wall_west:
        wall_west = S[r, c - 1]
    if isWall(wall_north): 
        if isWall(wall_east) or isWall(wall_west):
            return True
    elif isWall(wall_south):
        if isWall(wall_east) or isWall(wall_west):
            return True
    return False

    

# h2 takes a state and iterates through each index in the state. For every box in the state, it adds the
# the distance to the nearest goal to a variable. If there is a deadlock, then a large value is added to 
# the sum.
def h2(s):
    sumDist = 0
    dimRow = s.shape[0]
    dimCol = s.shape[1]
    for r in range(dimRow):
        for c in range(dimCol):
            if isBox(s[r, c]) or isBoxstar(s[r, c]):
                dist = find_dist_to_goal(s, r, c)
                sumDist += dist
                if isDeadlocked(s, r, c):
                    sumDist += 10000
    return sumDist



